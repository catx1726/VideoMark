export interface Mark {
  id: string // 唯一ID
  url: string // 标记所在的页面URL
  text: string // 高亮的文本内容，视频标记时存储格式化时间如 "12:34"
  note: string // 备注
  color: string // 高亮颜色
  html?: string // 新增：高亮内容的 HTML 格式
  rangySerialized?: string // Rangy 序列化后的选区字符串（视频标记时可选）
  shadowHostSelector?: string // 新增：如果高亮在 Shadow DOM 中，则存储其宿主元素的选择器
  createdAt: number // 创建时间戳
  title?: string // 网页 tab 名称
  domIndex?: number // 新增：用于物理位置排序，视频标记时可用 timestamp 替代
  tags?: string[] // 新增：关联的标签 ID 列表
  contextTitle?: string
  contextLevel?: number
  contextSelector?: string
  contextOrder?: number
  surroundingSnippet?: string
  deletedAt?: number // 新增：删除时间戳，用于同步删除

  // --- 视频标记相关字段 ---
  type?: 'text' | 'video' // 标记类型：text（默认）| video
  timestamp?: number // 视频时间点（秒）
  duration?: number // 视频总时长（秒）
  isLive?: boolean // 是否直播
  platform?: string // 平台标识（youtube, bilibili, twitch, generic...）
  videoSrc?: string // 视频源地址
  screenshot?: string // base64 缩略图（JPEG data URL）
}

export interface RemoveMarkPayload {
  url: string
  id: Mark['id']
  [key: string]: any
}

export interface UpdateMarkNotePayload {
  url: string
  id: Mark['id']
  note: string
  [key: string]: any
}

export interface GetMarkByIdPayload {
  url: string
  id: Mark['id']
  [key: string]: any
}
